<h1>Didukung Google, Guru Dilatih Kembangkan Kurikulum K12 Informatika</h1>
<br>
	 <img src="img/k.jpeg" width="600" heigth="800">
    
     <p>Sebanyak 46 guru SD, SMP dan SMA perwakilan seluruh DIY diberikan pendampingan 
     untuk mengembangan kurikulum mata pelajaran informatika atau dikenal dengan 
     kurikulum K12 informatika. Guru-guru tersebut memasuki tahap melaporkan perkembangan 
     penerapan kurikulum melalui seminar yang digelar di Fakultas Teknologi Industri Universitas 
     Islam Indonesia (UII), Kamis (8/8/2019).</p>
   
     <p>Dosen Prodi Teknik Informatika Pascasarjana Fakultas Teknologi Industri UII Hanson Prihantoro 
     menjelaskan jumlah guru informatika itu diikuti perwakilan dari 46 sekolah baik SD, 
     SMP dan SMA seluruh DIY, dosen serta perwakilan Dinas Pendidikan seluruh DIY. 
     Sebelum evaluasi kali ini, pihaknya telah memberikan beragam pelatihan 
     kurikulum K12 dan sudah dipraktikkan di sekolah masing-masing.</p>
     
     <p>“Dari pertemuan ini kami akan menampung berbagai masukan dan kendala agar formulasi 
     kurikulum ini bisa berjalan dengan baik terutama di semester berikutnya,” terangnya Jumat (9/8/2019).</p>
     
     <p>Pertemuan itu untuk mendiseminasikan persiapan kurikulum informatika K12 harapannya bisa menjadi
	  referensi sekolah yang lain dalam menerapkan pelajaran informatika yang lebih disenangi siswa. 
	  Mengingat dalam pertemuan itu juga diwakili sekolah lain yang belum tersasar program hibah penerapan kurikulum K12.</p>
	 
	  <h1>Hibah Persiapan Implementasi Kurikulum K12 Informatika bagi Guru Sekolah Dasar dan Menengah</h1>
	 
	  <img src="img/l.jpeg" width="600" heigth="800">
	  <p>Pada hari Selasa, 18 Juni 2019 di Ruang Multimedia Universitas Sanata Dharma Kampus 3 Paingan telah 
	  ditandatangani kontrak hibah bernilai total Rp 86,9 juta oleh 46 guru informatika dari 7 SD, 21 SMP, 
	  dan 18 SMA di wilayah Provinsi DIY. Dana tersebut dihibahkan oleh Google Indonesia melalui Biro Bebras Indonesia, 
	  sebagai lembaga nirlaba tingkat nasional yang selama ini banyak bergerak dalam edukasi bidang berpikir komputasional, 
	  guna mendukung persiapan implementasi kurikulum informatika di tingkat sekolah dasar dan menengah. Pengelolaan kegiatan 
	  dalam konteks hibah tersebut ditangani oleh 4 Biro Bebras Universitas yaitu Biro Bebras Universitas Sanata Dharma, 
	  Biro Bebras Universitas Atma Jaya Yogyakarta, Biro Bebras Universitas Gadjah Mada, dan Biro Bebras Universitas Islam Indonesia.</p>
	
	  <p>para guru mendapat kesempatan untuk menyusun perangkat pembelajaran sesuai kurikulum K12 informatika, 
	  dengan pendampingan dari para dosen program studi informatika yang berasal dari 4 Biro Bebras Universitas di DIY tersebut. 
	  Rencana pembelajaran tersebut selanjutnya akan  diimplementasikan di masing-masing sekolah pada semester gasal 2019/2020.</p>
	 
	  <img src="img/m.jpeg" width="600" heigth="800">
	  
	  <p>Persiapan implementasi kurikulum K12 informatika dipandang penting untuk dilakukan karena setelah sebelumnya 
	  mata pelajaran (mapel) Teknologi Informasi dan Komunikasi (TIK) sempat dihapus dari kurikulum. 
	  Kementerian Pendidikan dan Kebudayaan mulai tahun ajaran 2019/2020 menetapkan bahwa mata pelajaran 
	  informatika dapat diimplementasikan kembali di jenjang pendidikan sekolah dasar dan menengah. 
	  Karena mencakup 12 tahun masa pendidikan, rumusan kurikulum tersebut dikenal dengan istilah kurikulum K12 informatika. 
	  Dalam rancangan kurikulum sesuai Permendikbud tersebut, istilah mapel informatika dipergunakan untuk menggantikan
	  istilah mapel TIK. Secara umum, tatanan materi pengetahuan informatika digambarkan seperti struktur suatu bangunan,
	  di mana berpikir komputasional (Computational Thinking) menjadi pondasi bangunan yang menjadi landasan berpikir
	  yang menopang pengetahuan informatika. Bangunan tersebut memiliki lima pilar yaitu Sistem Komputer, Jaringan Komputer, 
	  Analisis dan Sains Data, Algoritma Pemrograman, dan  Aspek Sosial TIK-Informatika.</p>
	
	
	
	<p>tema materi informatika untuk  SD adalah “Computing for fun” dimana peserta didik diajak mengenal dan mulai 
	menggunakan komputer secara menyenangkan. Di tingkat SMP, tema yang diangkat adalah Computing as Tools and foundation, 
	dimana sistem komputer dan aplikasi mulai dipakai peserta didik secara lebih serius untuk menunjang proses pembelajaran, 
	dan dikenalkan aspek keilmuannya dalam bentuk visual. Sementara di tingkat SMA temanya adalah Computer Science, 
	dimana peserta didik mendapat landasan dalam bidang informatika (ilmu komputer) yang lebih konseptual.</p>
	<img src="img/n.jpeg" width="600" heigth="800">
	<p>Koordinator Biro Bebras Universitas Sanata Dharma, P.H. Prima Rosa, M.Sc.Mengungkapkan,
	 penyelenggaraan hibah ini dimaksudkan untuk mendukung Dinas Pendidikan serta sekolah dalam meningkatkan 
	 kompetensi guru informatika Indonesia, khususnya di daerah DIY dan sekitarnya, agar guru mampu mengembangkan 
	 perangkat pembelajaran pendukung implementasi kurikulum K-12 Informatika yang sejalan dengan upaya untuk 
	 membantu siswa mencapai kemampuan berpikir kritis, kreatif, serta mampu berkolaborasi dan berkomunikasi 
	 dengan baik khususnya di bidang informatika.</p>
	<p>Kemunculan mata pelajaran Informatika menjadi salah satu langkah strategis Kementrian Pendidikan dan 
	Kebudayaan dalam menghadapi tantangan revolusi industri 4.0. Mata pelajaran Informatika menjadi ilmu 
	yang seharusnya dikuasai para peserta didik di pendidikan dasar dan menengah. “Tentunya kemampuan 
	problem solving tidak bisa diperoleh begitu saja, tetapi perlu dibentuk dan dipupuk melalui pendidikan baik di rumah, 
	di sekolah, maupun juga di masyarakat.“ jelas Hanson.</p>
