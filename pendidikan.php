<h1>Infrastruktur Kurikulum “Merdeka Belajar” dan Pendidikan 4.0</h1>
<br>
<img src="img/j.jpeg" width="600" heigth="800">
<br>

<p>Ada tiga syarat infrastruktur dasar yang harus disiapkan oleh sekolah untuk bisa 
menyelenggarakan kurikulum pendidikan 4.0. Pertama, jaringan internet stabil dan 
berkecepatan tinggi; kedua, instrumen pembelajaran berbasis digital; dan ketiga, 
guru atau mentor profesional dan berkualitas.</p>

<p>Bilamana ada satu indikator dasar saja yang belum terpenuhi, sekolah dipastikan akan gagal
 dalam menyelenggarakan kurikulum sesuai standar pendidikan 4.0 dan merdeka belajar seperti yang diharapkan.</p>
<p>Namun, pembangunan infrastruktur pendidikan yang berbasis teknologi adalah investasi yang tidak murah. 
Oleh karena itu, sekolah harus segera menyiapkan infrastruktur tersebut secara bertahap dan terencana. 
Khususnya dalam hal dana yang akan dianggarkan.</p>
<p>Untuk itu, Pintek sebagai perusahaan inovasi keuangan yang memiliki fokus pada pengembangan pendidikan 
Indonesia membuat program pinjaman modal kerja dan pendanaan PO khusus untuk pengembangan infrastruktur 
sekolah yang lebih baik.</p>
