	 <p>Pemerintah melalui Kemendikbud telah memulai revolusi pendidikan sejak 2019 lalu, baik di tingkat dasar,
      menengah, hingga tinggi. Konsep yang diusung dalam revolusi ini adalah merdeka belajar di semua aspek pendidikan formal.</p>
	 <p>Namun, tampaknya masih banyak pihak yang meragukan apakah Indonesia benar telah siap dalam penerapan 
     sistem merdeka belajar ini. Salah satu alasan paling banyak didiskusikan adalah infrastruktur pendidikan.</p>
	<p>Bila Anda masih ingat proses peralihan kurikulum 1994 menjadi kurikulum 2004 (KBK), sebetulnya tantangan 
    di era sekarang pun masih sama. KBK dibuat dengan tujuan agar SDM siswa mampu bersaing dalam era globalisasi industri.</p>
	
<p>Oleh karena itu, pemerintah menganggarkan ratusan triliun APBN dengan fokus pembangunan infrastruktur IT 
di sekolah-sekolah. Pada rentang tahun 2004 dan 2006 pulalah sekolah wajib menyelenggarakan mata pelajaran komputer.</p>
<p>Namun, pada tahun 2020 ini dunia dihadapkan dengan tantangan baru, yakni industri 4.0. Kita telah 
masuk ke era baru industri yang biasa disebut dengan data technology. Pada titik ini, 
hampir semua aspek kehidupan akan bergantung pada teknologi, khususnya machine learning, AI, dan robot.</p>
<p>Lalu, sebenarnya apa itu merdeka belajar dan bagaimana konsep tersebut bisa efektif dalam 
sistem pendidikan 4.0 yang akan segera diterapkan di semua level pendidikan? Berikut pembahasannya:</p>
<br>
<h1>Konsep Merdeka Belajar dan Pendidikan 4.0</h1>
<br>
<img src="img/h.jpeg" width="600" heigth="800">
<br>
<p>Konsep merdeka belajar sangatlah berbeda dengan kurikulum yang pernah ada dan digunakan 
oleh pendidikan formal di Indonesia. Konsep pendidikan baru ini sangat memperhitungkan 
kemampuan dan keunikan kognitif individu para siswa. Berikut garis besar konsepnya:</p>
<lu>
<p><li>Asesmen kompetensi minimum</li></p>
<br>
<p>Perbedaan konsep pendidikan baru ini dengan kurikulum yang digunakan sebelumnya adalah, 
siswa diharapkan mampu menunjukkan kemampuan minimum dalam hal “literasi” dan “numerik.”</p>
<p>Fokusnya bukanlah sebanyak apa siswa mampu mendapatkan nilai melalui penugasan
 dari guru, tetapi bagaimana siswa mampu berpikir secara kritis menggunakan kemampuan kognitifnya</p>
<p>Dalam bidang literasi misalnya, bila pada kurikulum sebelum-sebelumnya siswa lebih banyak 
diharapkan menghafal dan menerapkan materi yang mereka baca, dalam konsep asesmen kompetensi, 
siswa diharapkan bisa berpikir logis untuk mengabstraksi maksud dan tujuan dari materi.</p>
<p>Begitu juga dalam hal “numerik” atau pada pelajaran sains seperti fisika, kimia, 
khususnya matematika. Siswa tidak boleh hanya menghafal formula atau rumus, tetapi juga 
menemukan konsep dasarnya, sehingga mereka bisa menerapkannya untuk penyelesaian masalah 
yang lebih luas.</p>
<br>
<li><p>Survei karakter</li></p>
<br>
<p>Cukup melegakan bahwa pada akhirnya pemerintah mengakui pendidikan di Indonesia 
adalah investasi yang mahal. Sebab, setiap daerah memiliki keunikan manusia yang 
berbeda-beda dan tidak mungkin dipaksa untuk menerapkan satu sistem dengan indikator tetap.</p>
<p>Pada konsep survei karakter, pemerintah akan menilai secara menyeluruh terkait kualitas 
pendidikan di sekolah. Bukan hanya tentang hasil belajar, tetapi juga ekosistem dan 
infrastruktur pendidikan yang tersedia.</p>
<p>Dengan kata lain, pengembangan kualitas pendidikan bukan lagi tentang penerapan 
indikator kualitas tetap, tetapi berdasarkan data hasil survei terbaru terhadap sekolah.</p>
<br>
<li><p>Perluasan penilaian hasil belajar</li></p>
<br>

<p>Satu hal paling menarik dalam konsep “merdeka belajar” ini adalah adanya 
perluasan penilaian hasil belajar siswa yang tadinya hanya dari nilai ujian 
nasional, menjadi penugasan dan portofolio.</p>
<p>Kedepannya siswa akan diberikan ruang untuk bisa mengembangkan diri mereka 
sesuai minat dan bakat. Dengan cara ini, stigma siswa pintar dan bodoh diharapkan 
bisa segera dihilangkan. Sebab, manusia memiliki bakat alami yang berbeda-beda, 
dan tidak bisa ditentukan dengan tes formal.</p>
<p><li>Pemerataan kualitas pendidikan hingga ke 3T</li></p>
<p>Merdeka belajar juga dapat diartikan keadilan terhadap akses pendidikan 
yang setara bagi seluruh siswa di Indonesia. Oleh karena itu, pemerintah 
membuat kebijakan afirmasi dan pemberian kuota khusus bagi siswa yang tinggal di daerah 3T.</p>
<p>Industri 4.0 adalah momen penting dalam pemerataan kualitas pendidikan di Indonesia. 
Sebab, pada tahun 2030 nanti akan menjadi puncak dari bonus demografi Indonesia 
dengan 64% penduduk adalah angkatan kerja.</p>

<p>Kesiapan sumber daya manusia (SDM) Indonesia akan sangat menentukan keberhasilan 
kita dalam menghadapi persaingan di industri 4.0. Khususnya di daerah 3T yang masih 
memiliki tingkat kelahiran yang sangat tinggi.</p><lu>
<br>
<h1>Alasan Konsep Merdeka Belajar Cocok Untuk Pendidikan 4.0</h1>
<br>
<img src="img/i.jpeg" width="600" heigth="800">
<br>
<p>Pemerintah mengusung konsep merdeka belajar bukan tanpa alasan dan fokus yang jelas. 
Pada 2021 nanti, kurikulum ada diubah total untuk menjamin sistem pendidikan 4.0 
dapat berjalan dengan baik. Lalu, apa dasar kecocokan konsep ini terhadap 
pendidikan 4.0? Sebagai berikut:</p>
<lu>
<p><li>Pengembangan pola pikir</li></p>

<p>Konsep pendidikan “merdeka belajar” memiliki fokus pada pengembangan kemampuan 
kognitif siswa. Artinya, siswa akan ditantang untuk mampu berpikir kritis dengan analisis yang baik.</p>

<p>Kemampuan inilah yang dibutuhkan siswa agar bisa membuat keputusan yang bijak dalam penyelesaian masalah. 
Sebab, dalam industri 4.0 basisnya adalah data technology dengan kata lain informasi yang bisa diakses oleh semua orang.</p>
<p>Siswa yang tidak mampu menganalisis semua informasi tersebut tentu akan gagal membuat analisis serta 
kesimpulan yang benar dan akurat. Hal ini tentu akan menjadi masalah ketika para siswa masuk ke dunia 
industri yang telah mengadopsi machine learning dan kecerdasan buatan (AI).</p>
<p><li>Inovasi di tingkat pendidikan</li></p>
<p>Salah satu pokok dari konsep pendidikan baru ini adalah membuat siswa mampu mengembangkan minat 
dan bakatnya di sekolah. Oleh karena itu, pemerintah menghapus penilaian melalui UN, dan 
menggantinya menjadi penugasan dan portofolio.</p>

<p>Alasannya jelas, siswa akan ditantang untuk mampu berinovasi terhadap instrumen dan penyelesaian masalah. 
Fokusnya adalah bagaimana siswa mampu menjawab persoalan dalam bentuk proyek mata pelajaran dari sekolah.</p>

<p>Proses ini penting bagi para siswa untuk belajar mengaplikasikan teori yang mereka pelajari di kelas 
menjadi sebuah hasil yang nyata. Siswa akan belajar membuktikan, bukan hanya menghafal materi.</p>

<p><li>Meningkatkan kecerdasan siswa</li></p>

<br>
<p>Tahukah Anda, berapa ranking PISA Indonesia tahun 2019? 74, atau urutan 6 terbawah dari 79 negara yang disurvei. 
Dari data tersebut saja, kita bisa menggambarkan betapa rendahnya kemampuan kognitif atau kecerdasan anak-anak Indonesia.</p>

<p>Dalam kurikulum pendidikan 4.0, pemerintah telah merancang standar khusus agar siswa Indonesia semakin 
terlatih kemampuan kognitifnya, dan semakin mampu menyelesaikan masalah dengan baik.</p>
</lu>
