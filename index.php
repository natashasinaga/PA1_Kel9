	<h1> Pendahuluan </h1>
	<p>Informatika atau komputer sains adalah mata pelajaran yang penting bagi pendidikan sekolah
		saat ini. Informatika dapat disajikan sebagai disiplin ilmu untuk memahami teknologi dengan
		cara yang lebih mendalam dibalik program komputer. Dengan dibawanya Informatika ke
		sekolah adalah mempersiapkan kaum muda untuk menjadi kreator teknologi informasi bukan
		hanya sekedar pengguna teknologi informasi. Untuk mencapai hal tersebut, perlu dikenalkannya
		konsep informatika mulai dari pendidikan dasar, sekolah menengah, dan juga sekolah menengah
		atas. Di lain pihak, kita perlu membantu orang untuk memecahkan masalah dengan
		menggunakan teknologi dan mengembangkan pemikiran komputasi di berbagai bidang.
		Makalah ini menyajikan tentang peran Indonesia secara khusus Fakultas Ilmu Komputer
		Universitas Dian Nuswantoro yang ada di kota Semarang dalam memperkenalkan pemikiran
		komputasi melalui Bebras Computational Thinking.</p>

	<p>Saat ini, komputer sudah merasuki hampir di semua bidang, sehingga Computational
	Thinking (CT) sudah menjadi kemampuan yang wajib untuk dikuasai oleh manusia di abad ke21 ini. 
	Sejak tahun 2014, beberapa negara menghapus mata pelajaran TIK di sekolah, dan
	menggantikannya dengan pelajaran informatika yang lebih bersifat keilmuan. Google, code.org,
	dan ACM juga ikut mendukung lahirnya K12 Curriculum Framework, yang saat ini notabene
	telah diimplementasikan menjadi standar kurikulum untuk sekolah dasar dan menengah negara
	maju. Standar kurikulum K12 yang dirintis oleh ACM dan CSTA menjadi salah satu aspek yang
	menjadi target kompetensi dalam kurikulum tersebut. Indonesia sudah mengadaptasi K12 CS Curriculum tersebut 
	untuk diimplementasikan sebagai kurikulum nasional pendidikan dasar dan
	menengah, yang sudah disahkan pada tanggal 20 Desember 2018 sebagai Peraturan Menteri
	Pendidikan dan Kebudayaan no 37 tahun 2018 sebagai perubahan dari Permendikbud nomor 24
	tahun 2016</p>

	<h1> Guru Teknologi Informasi/ Informatika K-12 </h1>
	<p>Guru adalah salah satu pemain kunci terpenting di dunia pendidikan. Sistem pendidikan, dan
	mereka menerapkan dan menyampaikan kurikulum di sekolah sehingga pendapat mereka
	tentang kurikulum Informatika sangat penting untuk perbaikan di masa depan. Guru juga perlu
	tahu bagaimana mengenali dan memecahkan masalah yang mungkin timbul di dalam kelas
	ketika mereka menyampaikan kurikulum.
	Dagiene dalam penelitiannya [6] melakukan survey terhadap guru IT/ Informatika dan
	mendapat jawaban dari 337 guru IT / Informatika (sekitar 35 persen dari semua guru IT /
	Informatika). 90% guru memiliki lebih dari 6 tahun praktik dalam mengajarkan disiplin ini,
	sementara sekitar 3 persen dari guru memiliki praktik kurang dari 3 tahun.
	Para guru diminta untuk menyatakan bagaimana kurikulum TI / Informatika harus diperbaiki
	atau diubah. Seperti yang kita lihat, banyak guru menekankan pemrograman pengajaran pada
	tahap awal dan terus menerus. Guru memperhatikan bahwa banyak topik yang berhubungan
	dengan IT harus diajarkan pada usia yang lebih muda sehingga siswa dapat menggunakan
	keterampilan ini untuk pembelajaran yang lebih baik dari mata pelajaran lain. Tiga modul
	opsional (Pemrograman, Desktop publishing, dan pengembangan dan manajemen basis data)
	untuk tingkat menengah atas dibahas secara rinci dalam penelitiannya. Guru menyatakan sikap
	mereka tentang informatika sebagai mata pelajaran mendasar yang wajib bagi siswa dari segala
	usia: 161 guru sangat setuju dengan itu. Hampir semua guru IT / Informatika sepakat bahwa
	pengetahuan dasar informatika harus dikirimkan untuk setiap siswa di K-12 karena masyarakat
	membutuhkan lebih banyak pembuat teknologi, bukan hanya pengguna. Fisika misalnya
	menjelaskan hukum realitas, dan Informatika menjelaskan hukum realitas virtual. Siswa perlu
	berpikir secara algoritmik ketika menganalisis data dari kehidupan sehari-hari, siswa perlu
	berpikir secara komputasi ketika memecahkan masalah. Salah satu kegiatan yang dilakukan oleh Biro Bebras Udinus adalah melakukan sosialisasi kepada guru-guru IT yang ada di kota
Semarang. Guru-guru sangat antusias dengan kegiatan yang sudah berjalan 2 tahun ini. </p>

