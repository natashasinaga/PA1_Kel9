	<h1>Masalah Pendidikan ICT di Indonesia</h1>
<br>
<img src="img/d.jpeg" width="600" heigth="800">
<br>
<p>Di Indonesia secara umum, pendidikan ICT di sekolah-sekolahnya masih cukup tertinggal. 
Selain karena kurangnya fasilitas penunjang, kurangnya guru yang cukup berkompeten juga jadi masalah utama.
ICT adalah hal utama pada revolusi industri 4.0. Apa yang dipelajari di sekolah seharusnya bisa relevan 
dengan alur perkembangan ICT yang terjadi di dunia.
Berkaca pada hal tersebut, pemerintah berinisiasi untuk mentransformasi konsep belajar dan 
pembelajaran mapel ICT di sekolah.
Seiring berjalannya waktu, “Kurikulum K12 Informatika” digagas.
Pada kurikulum tersebut, mapel informatika akan diajarkan selama 12 tahun masa pendidikan. 
Dengan kata lain, mapel informatika bisa diajarkan sejak sekolah dasar.<
Sekolah-sekolah dari jenjang SD – SMA diharapkan bisa mempersiapkan diri untuk bisa menerapkan program ini.
Konsep tersebut terinspirasi dari K-12 Computer Science Framework yang dikeluarkan oleh ACM melalui CSTA.
Kurikulum tersebut sudah diaplikasikan oleh sejumlah sekolah di US dan terbukti menunjukan hasil positif.
Pada mapel informatika, konsep pembelajarannya akan dikembangkan.
Sejak dini, peserta didik akan dibiaskan dengan computational thinking dan computing practice.</p>
<br>
<h1>Model Pembelajaran Kurikulum K12 Informatika.</h1>
<br>
<img src="img/e.jpeg" width="600" heigth="800">
<br>
<p>Ilustrasi di atas adalah gambaran umum tentang tatanan materi pada mapel informatika.
Transformasi pendidikan informatika yang ingin dicapai berlandaskan pada teori belajar 
konstruktivisme. Pada teori belajar ini, siswa ditanamkan paradigma pembelajaran yang 
mengedepankan logika dan juga konseptual.
Konsep mengajar sedikit digeser. Guru tidak lagi jadi pusat, pembelajaran akan berpusat pada peserta didik.
Model pembelajaran yang perlu diaplikasikan oleh pihak sekolah meliputi:</p>
<br>

<h2>1. Problem-based Learning</h2>
<p>Peserta didik dihadapkan pada suatu problem atau permasalahan. 
Secara berkelompok, mereka diminta untuk melakukan eksplorasi dan analisa.
Berdasarkan hasil eksplorasi dan analisa, peserta didik diharapkan bisa menemukan solusi.
Solusi yang dihasilkan tentu saja harus berhubungan dengan cakupan ilmu informatika.</p>
<br>
<h2>2. Project-based Learning</h2>
<p>Peserta didik diarahkan untuk mengerjakan suatu proyek berbasis informatika.
Proyek yang diberikan bisa terkait dengan mata pelajaran lain.
Pada level basic, proyek bisa berupa pemanfaatan ICT. Bagaimana peserta didik bisa 
memanfaatkan ICT untuk menunjang pembelajaran mata pelajaran lain.
Pada level yang lebih advance, proyek bisa berupa pembuatan produk digital.
Produk digital bisa berupa produk nyata dengan sejumlah komponen hardware, 
atau produk maya berupa aplikasi atau software.</p>
<br>
<h2>3. Inquiry/discovery Learning</h2>
<p>Pada metode pembelajaran ini, peserta didik dipicu untuk berpikir secara kritis. 
Peserta didik dididik untuk bisa berlaku analitis, membuat rumusan masalah, 
dan bisa memberi solusi pada masalah.
Metode ini tidak hanya akan merangsang pemikiran eksak, peserta didik 
juga bisa dipicu kreativitas dan kemampuannya berimajinasi.
Metode pembelajaran tersusun dalam dua proses utama:
<br>
1. Peserta didik merumuskan suatu pertanyaan atau masalah (to inquire).
<br>
2. Peserta didik menyingkap dan menemukan solusi atau jawaban dari pertanyaan atau masalah tersebut (to discover).</p>
<br>

<h2>4. Integrasi dengan STEAM</h2>
<p>STEAM adalah suatu tema gerakan reformasi pendidikan. Sebuah ide yang menekankan bahwa bidang sains, 
teknologi, engineering, seni/art, dan matematika harus didorong pada pelaksanaan pendidikan.
Masing-masing bidang pada STEM ini harus bisa terintegrasi.
Jika hal tersebut berhasil dicapai, pendidikan bisa berjalan dengan lebih optimal. 
Program pendidikan bisa menghasilkan SDM yang mampu bersaing secara global dalam inovasi iptek.
Dengan STEAM, peserta didik juga akan dilatih untuk menguasai keterampilan abad 21 6, yaitu:
<br>
1. Communication
<br>
2. Collaboration
<br>
3. Creativity
<br>
4. Critical thinking
<br>
5. Character
<br>
6. Connectivity
<br>
<h2>Implementasi Program di Sekolah</h2>
<br>
<img src="img/f.jpeg" width="600" heigth="800">
<p>Program ini tidak akan bisa berjalan dengan baik jika pihak sekolah tidak meresponnya dengan tepat.
Berikut adalah panduan implementasi program ini.</p>
<h1>1. Persiapan</h1>
<p>Pada tahap persiapan, guru harus mempelajari bahan-bahan yang dibutuhkan dalam pembelajaran, meliputi:
<br>
- Kurikulum dan juga model silabus,
<br>
- Materi ilmu informatika,
<br>
- Standar proses untuk merancang silabus dan RPP,
<br>
- Sarana dan prasarana yang diperlukan untuk menunjang pembelajaran,
<br>
- Sumber-sumber materi pembelajaran yang terpercaya.</p>
<br>

<h2>2. Perancangan Pembelajaran</h2>

<p>Pada tahap ini, guru mulai merancang tujuan pembelajaran. Guru harus merumuskan konsep belajar mengajar yang ingin digunakan.
Materi pembelajaran yang akan diberikan juga harus disusun.
Selain itu, guru juga harus merumuskan metode pembelajaran untuk menyampaikan materi pelajaran.
Pada tahap ini, tim guru beserta kepala sekolah juga harus mengidentifikasi sarana penunjang 
yang dibutuhkan dan apa saja yang mungkin untuk disediakan.
Jika pembelajaran ingin lebih optimal, sekolah perlu melakukan pengadaan sarana dan prasarana penunjang yang lebih baik.
Pintek bisa membantu pihak sekolah untuk memenuhi hal tersebut. Ada produk Pinjaman 
Modal Kerja yang bisa dimanfaatkan sekolah untuk pengadaan fasilitas penunjang yang lebih baik.
Skema Pinjaman Modal Kerja dari Pintek memiliki bunga ringan, dan bisa 
mencairkan dana hingga miliaran rupiah.</p>

<h2>3. Pengembangan Bahan Ajar</h2>
<p>Pada tahap ini guru dituntut untuk mengumpulkan bahan ajar diluar yang sudah ditetapkan.
Bahan ajar penunjang ini bisa dalam bentuk slide kreatif, video, dll.</p>

<h2>4. Pelaksanaan Pembelajaran</h2>
<p>Guru melaksanakan pembelajaran mengacu pada RPP final dan menggunakan bahan ajar yang sudah dikumpulkan sebagai penunjang.
Pada tahap ini juga dilaksanakan evaluasi bagi peserta didik (UTS atau UAS).
Pada tahap ini, guru dan peserta didik juga harus menghasilkan portofolio.</p>

<h2>5. Evaluasi</h2>
<p> guru yang melakukan evaluasi pembelajaran, Kepala sekolah juga melakukan evaluasi lebih luas. 
Seluruh perangkat pembelajaran dievaluasi.
Berdasarkan evaluasi tersebut, rekomendasi perbaikan akan dihasilkan.
Seluruh pihak harus bisa saling bersinergi untuk menerapkan secara optimal Kurikulum K12 Informatika ini.
Program ini diharapkan bisa merubah konsep belajar anak-anak Indonesia tentang ICT. Jika bisa tercapai, 
bukan tidak mungkin, Indonesia akan mencetak banyak SDM unggulan.</p>

